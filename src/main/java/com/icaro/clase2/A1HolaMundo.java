package com.icaro.clase2;

public class A1HolaMundo {


    public static void main(String[] args) {

        System.out.println("Hola Mundo!"); // Imprime String
        System.out.println(10); // Imprime un entero
        System.out.println(20.56); // Imprime un flotante
        System.out.println(true); // Imprime un booleano
        System.out.println(false);
        System.out.println('c'); // Imprime un caracter

        //Concatenacion
        System.out.println("Hola " + "mundo, hoy es: " + 31);

    }
}
